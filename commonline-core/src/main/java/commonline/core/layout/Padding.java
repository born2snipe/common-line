package commonline.core.layout;

/**
 * Represents the type of padding the field has
 */
public class Padding {
    public static final Padding SPACES = new Padding();
    public static final Padding ZEROS = new Padding();

    private Padding() {
    }

    public String toString() {
        if (this.equals(SPACES)) {
            return "SPACES";
        }
        return "ZEROS";
    }
}
