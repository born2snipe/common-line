package commonline.core.layout;

/**
 * Represents the possible justifications a field maybe
 */
public class Justified {
    public static final Justified RIGHT = new Justified();
    public static final Justified LEFT = new Justified();

    private Justified() {
    }

    public String toString() {
        if (this.equals(RIGHT)) {
            return "RIGHT";
        }
        return "LEFT";
    }
}
