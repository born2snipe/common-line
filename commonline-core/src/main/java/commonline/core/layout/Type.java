package commonline.core.layout;

/**
 * Represents the data type of the field
 */
public class Type {
    public static final Type ALPHA_NUMERIC = new Type();
    public static final Type NUMERIC = new Type();

    private Type() {

    }

    public String toString() {
        if (this.equals(ALPHA_NUMERIC)) {
            return "ALPHA-NUMERIC";
        }
        return "NUMERIC";
    }
}
