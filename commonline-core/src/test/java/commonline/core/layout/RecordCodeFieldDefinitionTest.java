package commonline.core.layout;

import junit.framework.TestCase;


public class RecordCodeFieldDefinitionTest extends TestCase {
    public void test() {
        RecordCodeFieldDefinition definition = new RecordCodeFieldDefinition();

        assertNull(definition.getJustified());
        assertEquals(2, definition.getLength());
        assertEquals("Record Code", definition.getName());
        assertNull(definition.getPadding());
        assertEquals(0, definition.getPosition());
        assertEquals(Type.ALPHA_NUMERIC, definition.getType());
    }
}
